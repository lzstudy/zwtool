
#define GPIO1_DR 	            *((volatile unsigned int *)0X0209C000)          /**< GPIO数据寄存器 */

/**************************************************************************************************
 * @brief  : 1u秒延时
 * @param  : None
 * @return : None
**************************************************************************************************/
static void delay_us(volatile int n)
{
    for(int i = 0 ; i < n ; i++)
        for(int j = 0 ; j < 2000 ; j++)
        {
            ;
        }
}

/**************************************************************************************************
 * @brief  : 设置LED状态
 * @param  : None
 * @return : None
**************************************************************************************************/
static void led_set(int state)
{
    if(state == 1)
        GPIO1_DR &= ~(1<<3); 
    else
        GPIO1_DR |= (1<<3);
}


/**************************************************************************************************
 * @brief  : 主函数入口
 * @param  : None
 * @return : None
**************************************************************************************************/
int main(void)
{
    for(;;)
    {
        delay_us(1000);
        led_set(1);
        delay_us(1000);
        led_set(0);
    }

    return 0;
}